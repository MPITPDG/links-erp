-- === Backup current values before touching anything ===
IF OBJECT_ID('dbo.BACKUP_ACC_BANK_ZERO_20260718') IS NOT NULL DROP TABLE dbo.BACKUP_ACC_BANK_ZERO_20260718
IF OBJECT_ID('dbo.BACKUP_ACC_BANKDTLS_ZERO_20260718') IS NOT NULL DROP TABLE dbo.BACKUP_ACC_BANKDTLS_ZERO_20260718

SELECT	ID, ENTRYNO, ACTUALAMOUNT, GETDATE() AS BACKED_UP_ON
INTO	dbo.BACKUP_ACC_BANK_ZERO_20260718
FROM	ACC_BANK
WHERE	ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)

SELECT	ID, ENTRYNO, AMOUNT, DEDUCTION, GETDATE() AS BACKED_UP_ON
INTO	dbo.BACKUP_ACC_BANKDTLS_ZERO_20260718
FROM	ACC_BANKDTLS
WHERE	ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)

DECLARE @BankBackupCount INT, @DtlsBackupCount INT
SELECT @BankBackupCount = COUNT(*) FROM dbo.BACKUP_ACC_BANK_ZERO_20260718
SELECT @DtlsBackupCount = COUNT(*) FROM dbo.BACKUP_ACC_BANKDTLS_ZERO_20260718

PRINT 'Backed up ' + CONVERT(VARCHAR, @BankBackupCount) + ' ACC_BANK row(s) and '
	+ CONVERT(VARCHAR, @DtlsBackupCount) + ' ACC_BANKDTLS row(s).'

-- === Apply the fix ===
BEGIN TRANSACTION

UPDATE	ACC_BANK
SET		ACTUALAMOUNT = 0
WHERE	ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)

PRINT CONVERT(VARCHAR, @@ROWCOUNT) + ' row(s) updated in ACC_BANK.'

UPDATE	ACC_BANKDTLS
SET		AMOUNT = 0,
		DEDUCTION = 0
WHERE	ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)

PRINT CONVERT(VARCHAR, @@ROWCOUNT) + ' row(s) updated in ACC_BANKDTLS.'

COMMIT TRANSACTION
GO

