-- Master (bank) records
SELECT	b.ID,
		b.ENTRYNO,
		b.ENTRYDT,
		b.[STATUS],
		b.BANK,
		b.CHEQUETYPE,
		b.CHEQUENO,
		b.CHEQUEDT,
		b.OURBANK,
		b.NARRATION,
		b.CITYCODE,
		b.CMPCODE,
		b.CMPID,
		b.USERNAME,
		b.ACTUALAMOUNT,
		b.BANKREFNO,
		b.clearanceDate
FROM	ACC_BANK b
WHERE	b.ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)
ORDER BY b.ENTRYNO

-- Detail (child) records
SELECT	d.ID,
		d.ENTRYNO,
		d.CLIENT,
		d.JOBNO,
		d.BILLNO,
		d.AMOUNT,
		d.DEDUCTION,
		d.ENTRYTYPE,
		d.NARRATION,
		d.ITEMCODE,
		d.DeptId,
		d.EMP_CODE,
		d.USERNAME
FROM	ACC_BANKDTLS d
WHERE	d.ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)
ORDER BY d.ENTRYNO, d.ID

-- Combined view: master + aggregated detail totals
SELECT	b.ENTRYNO,
		b.ENTRYDT,
		b.[STATUS],
		b.BANK,
		b.CHEQUENO,
		b.NARRATION,
		b.ACTUALAMOUNT,
		ISNULL(t.TOTAMOUNT, 0)		TOTAMOUNT,
		ISNULL(t.TOTDEDUCTION, 0)	TOTDEDUCTION,
		ISNULL(t.TOTAMOUNT, 0) - ISNULL(t.TOTDEDUCTION, 0) NETTOTAL
FROM	ACC_BANK b
LEFT JOIN (
	SELECT	ENTRYNO,
			SUM(ISNULL(CONVERT(FLOAT, AMOUNT), 0))		TOTAMOUNT,
			SUM(ISNULL(CONVERT(FLOAT, DEDUCTION), 0))	TOTDEDUCTION
	FROM	ACC_BANKDTLS
	WHERE	ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)
	GROUP BY ENTRYNO
) t ON t.ENTRYNO = b.ENTRYNO
WHERE	b.ENTRYNO IN (SELECT ENTRYNO FROM @EntryNos)
ORDER BY b.ENTRYNO
GO
